function sum(a, b){
    return a + b;
}

function diff(a, b){
    return a - b;
}

function mult(a, b){
    return a * b;
}

function div(a, b){
    return a / b;
}

function exp(a, b){
    return a ** b;
}

function remain(a, b){
    return a % b;
}

module.exports = {sum, diff, mult, div, exp, remain};